train_data = []
for _, row in datos_historicos.iterrows():
    h = row['Equipo_Local']
    a = row['Equipo_Visitante']
    s_h = stats[stats['Equipo'] == h]
    s_a = stats[stats['Equipo'] == a]
    if s_h.empty or s_a.empty:
        continue
    s_h = s_h.iloc[0]
    s_a = s_a.iloc[0]
