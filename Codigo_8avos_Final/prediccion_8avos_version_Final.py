def predict_match(home, away):
    s_h = stats[stats['Equipo'] == home]
    s_a = stats[stats['Equipo'] == away]
    if s_h.empty or s_a.empty:
        return None
    s_h = s_h.iloc[0]
    s_a = s_a.iloc[0]

    match = {}
    for col in stats.select_dtypes(include=[np.number]).columns:
        match[f"{col}_diff"] = s_h[col] - s_a[col]
    X_pred = pd.DataFrame([match])
    X_pred_scaled = scaler.transform(X_pred)

    probs = model.predict_proba(X_pred_scaled)[0]
    idx_away = np.where(model.classes_ == -1)[0][0]
    idx_draw = np.where(model.classes_ == 0)[0][0]
    idx_home = np.where(model.classes_ == 1)[0][0]
    p_away = probs[idx_away]
    p_draw = probs[idx_draw]
    p_home = probs[idx_home]

    # Goles esperados (xG) usando promedios históricos ajustados por probabilidades
    avg_goals_home = s_h.get('avg_Goles_total', 1.2)
    avg_goals_away = s_a.get('avg_Goles_total', 1.2)
    lam_home = max(0.1, avg_goals_home * (0.8 + 0.4 * p_home))
    lam_away = max(0.1, avg_goals_away * (0.8 + 0.4 * p_away))

    g_h = int(round(lam_home))
    g_a = int(round(lam_away))
    g_h = max(0, g_h)
    g_a = max(0, g_a)

    # --- NUEVO: El ganador se decide por la mayor probabilidad ---
    if p_home > p_away:
        ganador = home
    elif p_away > p_home:
        ganador = away
    else:
        # En caso de empate perfecto de probabilidades, se usa el ranking FIFA
        if s_h['Puntuación'] >= s_a['Puntuación']:
            ganador = home
        else:
            ganador = away

    return g_h, g_a, ganador, p_home, p_draw, p_away, lam_home, lam_away

