def extract_teams(match_str):
    parts = re.split(r'\s*vs\.?|\s*[–—-]\s*', match_str)
    teams = [clean_name(p.strip()) for p in parts if p.strip()]
    if len(teams) >= 2:
        return teams[0], teams[1]
    return None, None

